public class AssignmentDemo {
    public static void main(String[] args) {
        int x = 3;
        int y = 2;

        x+=y;  //x= x+Y=8
        System.out.println("x = "+x);

        x-=y;  //x= x+Y=2
        System.out.println("x = "+x);

        x*=y;  //x= x*Y=15
        System.out.println("x = "+x);

        x/=y;  //x= x/Y=8
        System.out.println("x = "+x);

        x%=y;  //x= x/Y=8
        System.out.println("x = "+x);
    }
}
